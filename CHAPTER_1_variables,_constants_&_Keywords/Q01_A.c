// write c program to calculate area of rectangle
// using in built input in code

#include <stdio.h>

int main()
{
    printf("This program gives area of rectangle\n");

    // creating two variable length = 5 and breadth = 7
    int length = 5, breadth = 7;

    // printing value of length and breadth
    printf("The area of rectangle if length is = 5 and breadth is = 7\n");

    // printing area of rectangle
    printf("the area is %d", length * breadth);

    return 0;
}